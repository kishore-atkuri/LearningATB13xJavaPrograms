package ex_04_Operators;

public class Lab056_Interview {
    public static void main(String[] args) {
        short s = 10;
        char c = 'A';
        String s1 = "A";
        System.out.println(c+s); //10+65=75
        System.out.println(c); // A
        System.out.println(c+c); // 65+65=130
        System.out.println(c+s1); // AA
    }
}
